<!-- array()
تنشا مصفوفة -->

    $arr = array(11, 2, 53);

 -----------------------------   
إضافه الى نهايه المصفوفة
array_push()

    $arr = [1,2];
    array_push($arr, 3); // [1,2,3]

--------------------------------
لحدف اخر عنصر في المصفوفه
array_pop()
    array_pop([1,2,3]); // يرجعي ويحذف 3

--------------------------------
إضافة عنصر أو أكثر إلى بداية المصفوفة

array_unshift()


    $arr=[2,3];
    array_unshift($arr,1); // [1,2,3]

--------------------------------
    حذف أول عنصر
array_shift()

    array_shift([1,2,3]); // يرجع 1

--------------------------------

تحقق إذا كانت القيمة موجودة داخل المصفوفة

in_array()
    in_array(2,[1,2,3]); // true

--------------------------------
التحقق من وجود مفتاح
array_key_exists()

    array_key_exists('name',['name'=>'Ali']); // true

    --------------------------------
    ترجع جميع المفاتيح

array_keys()


    array_keys(['a'=>1,'b'=>2]); // ['a','b']


    --------------------------------
    ترجع جميع القيم

array_values()


    array_values(['a'=>1,'b'=>2]); // [1,2]

    --------------------------------

array_merge()

دمج أكثر من مصفوفة

    array_merge([1,2],[3,4]); // [1,2,3,4]


    --------------------------------

    تجزئة جزء من المصفوفة
array_slice()


    array_slice([1,2,3,4],1,2); // [2,3]

    --------------------------------

    حذف أو استبدال جزء من المصفوفة
array_splice()


    $arr=[1,2,3,4];
    array_splice($arr,1,2); // المصفوفة تصبح [1,4]


    --------------------------------


    البحث عن قيمة وإرجاع مفتاحها
array_search()


    array_search(3,[1,2,3]); // 2


    --------------------------------


    عكس ترتيب المصفوفة
array_reverse()


    array_reverse([1,2,3]); // [3,2,1]


    --------------------------------

    إزالة القيم المكررة
array_unique()


    array_unique([1,1,2,3]); // [1,2,3]


    --------------------------------

    حساب مجموع القيم
array_sum()


    array_sum([1,2,3]); // 6


    --------------------------------


array_product()

حساب ناتج ضرب القيم

    array_product([2,3]); // 6



    --------------------------------


sort()

ترتيب تصاعديًا
    $arr=[3,1,2];
    sort($arr); // [1,2,3]

    --------------------------------

    ترتيب تنازلي
rsort()


    $arr=[1,3,2];
    rsort($arr); // [3,2,1]

    --------------------------------

    ترتيب تصاعدي مع الحفاظ على المفاتيح
asort()


    $arr=['b'=>3,'a'=>1];
    asort($arr); // ['a'=>1,'b'=>3]



    --------------------------------

arsort()

ترتيب تنازلي مع الحفاظ على المفاتيح

    arsort(['a'=>1,'b'=>3]); // ['b'=>3,'a'=>1]

    --------------------------------

    ترتيب حسب المفاتيح تصاعديًا

ksort()


    ksort(['b'=>2,'a'=>1]); // ['a'=>1,'b'=>2]

    --------------------------------

    ترتيب حسب المفاتيح تنازليًا
krsort()


    krsort(['a'=>1,'b'=>2]); // ['b'=>2,'a'=>1]

    --------------------------------


array_map()

تطبيق دالة على كل عنصر

    array_map(fn($n)=>$n*2,[1,2]); // [2,4]


    --------------------------------


    فلترة العناصر بناءً على شرط
array_filter()


    array_filter([1,2,3],fn($n)=>$n>1); // [2,3]

    --------------------------------

    تقليل المصفوفة إلى قيمة واحدة
array_reduce()

    array_reduce([1,2,3],fn($c,$n)=>$c+$n,0); // 6

    --------------------------------


implode()

تحويل مصفوفة إلى نص

    implode(",",[1,2,3]); // "1,2,3"

    --------------------------------

explode()

تحويل نص إلى مصفوفة

    explode("-", "a-b-c"); // ['a','b','c']
